package coo.base.constants;

/**
 * 计量用的常量。
 */
public class Measure {
  /** K字节数 */
  public static final Integer K = 1024;
  /** M字节数 */
  public static final Integer M = 1024 * K;
  /** G字节数 */
  public static final Integer G = 1024 * M;
  /** T字节数 */
  public static final Integer T = 1024 * G;

  private Measure() {}
}
